# Треугольник существует только тогда, когда сумма любых двух его сторон больше третьей.
# Дано a, b, c - стороны предполагаемого треугольника.
# Требуется сравнить длину каждого отрезка-стороны с суммой двух других.
# Если хотя бы в одном случае отрезок окажется больше суммы двух других,
# то треугольника с такими сторонами не существует.
# Отдельно сообщить является ли треугольник разносторонним, равнобедренным или равносторонним.

def inp():
    while True:
        try:
            string = input('\n Введите длины сторон треугольника через пробел: ').split()
            a = float(string[0])
            b = float(string[1])
            c = float(string[2])
            if a + b <= c or a + c <= b or b + c <= a:     # очень важно здесь поставить <= а не <
                print('\n Треугольник с длинами таких сторон не может существовать. Повторите попытку.')
                continue
            return a, b, c
        except:
            print('\n Вы ввели не цифровые значения длин сторон. Повторите попытку.\n')

a, b, c = inp()
if a == b == c:
    print('\nТреугольник равносторонний\n')
elif a == b or a == c or b == c:
    print('\n Треугольник равнобедренный\n')
else:
    print('\n Треугольник РАЗНОсторонний\n')


